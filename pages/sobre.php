                <h2>Nossa História</h2> 
                <p>
                Criado e desenvolvido inicialmente por dois graduandos do curso de Engenharia de Produção em uma instituição da
                região de Sorocaba, interior paulista. O Next Level surgiu através da demanda de um trabalho acadêmico com tema
                inovação, do qual inicialmente
                o atual CEO da empresa, o Sr. Vitor Hugo de Paula Pereira identificou uma necessidade em seu dia a dia que ocorria
                devido a ineficiência na divulgação de eventos acadêmicos e tecnológicos para o público universitário, afinal de
                contas, estudar
                e trabalhar necessita de um grande investimento do seu tempo pessoal, impondo barreiras para que se consiga dar a
                devida atenção aos eventos que são de nosso interesse profissional. Dessa forma precisava-se de uma ferramenta que
                fornecesse praticidade
                na informação da ocorrência de um evento.
                <br> E assim no início do ano de 2015 surgiu a ideia da criação de um App para a prestação desse tipo de serviço,
                divulgação de eventos acadêmicos com foco ao público estudantil.
                <br> Somente no ano de 2017 foi possível lançar o App Next Level, disponível para o Sistema Operacional Android,
                além de uma plataforma WebApp contendo as mesmas funcionalidades do aplicativo e assim atendendo os usuários do
                sistema iOS. Desde
                de 2015 até o ano de lançamento, os graduandos tiveram que desenvolver alguns trabalhos e pesquisas, que os
                fizessem validar o desenvolvimento desse App assim como a prestação de serviços de nossa empresa. Durante todo
                esse período o COO da empresa, o
                Sr. Jânio Carlos Santos Oliveira se fez presente e de suma importância no desenvolvimento e crescimento da empresa
                Next Level.
                <br>Para o desenvolvimento e amadurecimento da ideia houve a participação de outros colaboradores dentro da Equipe
                Next Level que se empenharam no crescimento e aprimoramento de nossa marca. À todos que fizeram e fazem parte
                dessa equipe, nossos mais sinceros agradecimentos.
                <br> Atualmente o Next Level vê e busca alcançar inúmeras oportunidades de disseminar seu trabalho, prevendo não
                ser somente uma aplicação mobile, e sim uma considerável plataforma de prestação de serviços em prol da
                multiplicação de conhecimento
